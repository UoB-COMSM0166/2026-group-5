(function () {
  const mapName = 'map2bomp_merged_colormap';
  const sourceMap = typeof TileMaps !== 'undefined' ? TileMaps[mapName] : null;

  if (!sourceMap || !Array.isArray(sourceMap.layers) || !sourceMap.layers.length) {
    return;
  }

  const tileLayer = sourceMap.layers.find((layer) => layer.type === 'tilelayer' && Array.isArray(layer.data));
  if (!tileLayer) {
    return;
  }

  const uniqueValues = Array.from(new Set(tileLayer.data.filter((value) => Number.isFinite(value) && value > 0))).sort((a, b) => a - b);
  const tileBaseGid = 200;

  function createColorTileDataUrl(color, label) {
    if (typeof document === 'undefined') {
      return 'data:image/gif;base64,R0lGODlhAQABAIAAAAAAAP///ywAAAAAAQABAAACAUwAOw==';
    }

    const canvas = document.createElement('canvas');
    canvas.width = 16;
    canvas.height = 16;
    const ctx = canvas.getContext('2d');

    ctx.fillStyle = color;
    ctx.fillRect(0, 0, 16, 16);
    ctx.strokeStyle = 'rgba(255, 255, 255, 0.35)';
    ctx.lineWidth = 1;
    ctx.strokeRect(0.5, 0.5, 15, 15);
    ctx.fillStyle = 'rgba(0, 0, 0, 0.55)';
    ctx.fillRect(1, 9, 14, 6);
    ctx.fillStyle = '#ffffff';
    ctx.font = 'bold 8px Arial';
    ctx.textAlign = 'center';
    ctx.textBaseline = 'middle';
    ctx.fillText(String(label), 8, 12);

    return canvas.toDataURL('image/png');
  }

  const tileValueToGid = new Map(uniqueValues.map((value, index) => [value, tileBaseGid + index]));
  const customTilesetConfig = Object.fromEntries(
    uniqueValues.map((value, index) => {
      const firstgid = tileBaseGid + index;
      const hue = (index * 47) % 360;
      const tileColor = value === 48
        ? '#000000'
        : value === 11
          ? '#d9d9d9'
          : value === 40
            ? '#ffffff'
            : `hsl(${hue}, 70%, 50%)`;
      return [firstgid, {
        name: `map2_color_${value}`,
        image: createColorTileDataUrl(tileColor, value),
        tileWidth: 16,
        tileHeight: 16,
        mapTileWidth: 16,
        mapTileHeight: 16,
        columns: 1,
        totalTiles: 1
      }];
    })
  );

  globalThis.CustomTilesetConfig = {
    ...(globalThis.CustomTilesetConfig || {}),
    ...customTilesetConfig
  };

  const remappedLayer = {
    ...tileLayer,
    data: tileLayer.data.map((value) => {
      if (!value) {
        return 0;
      }
      return tileValueToGid.get(value) || 0;
    })
  };

  const map2ColorData = {
    ...sourceMap,
    layers: sourceMap.layers.map((layer) => layer === tileLayer ? remappedLayer : { ...layer }),
    tilesets: uniqueValues.map((value, index) => ({
      firstgid: tileBaseGid + index,
      source: `generated/map2_color_${value}.tsx`
    }))
  };

  if (typeof TileMaps !== 'undefined') {
    TileMaps[mapName] = map2ColorData;
  }
})();

