  function cloneMatrix(matrix) {
    return Array.isArray(matrix) ? matrix.map((row) => row.slice()) : [];
  }

  function tileKey(x, y) {
    return `${x},${y}`;
  }

  function autoBuildRoomMatrix(collisionMatrix) {
    if (!Array.isArray(collisionMatrix) || !collisionMatrix.length) return [];
    const rows = collisionMatrix.length;
    const cols = collisionMatrix[0].length;
    const matrix = Array.from({ length: rows }, () => Array(cols).fill(1));
    const visited = Array.from({ length: rows }, () => Array(cols).fill(false));
    let roomId = 2;

    for (let y = 0; y < rows; y++) {
      for (let x = 0; x < cols; x++) {
        if (visited[y][x]) continue;
        visited[y][x] = true;
        if (collisionMatrix[y][x] === 1) {
          matrix[y][x] = 1;
          continue;
        }

        const queue = [{ x, y }];
        matrix[y][x] = roomId;
        for (let i = 0; i < queue.length; i++) {
          const node = queue[i];
          const neighbors = [
            { x: node.x + 1, y: node.y },
            { x: node.x - 1, y: node.y },
            { x: node.x, y: node.y + 1 },
            { x: node.x, y: node.y - 1 }
          ];
          for (const next of neighbors) {
            if (next.x < 0 || next.y < 0 || next.x >= cols || next.y >= rows) continue;
            if (visited[next.y][next.x]) continue;
            visited[next.y][next.x] = true;
            if (collisionMatrix[next.y][next.x] === 1) {
              matrix[next.y][next.x] = 1;
              continue;
            }
            matrix[next.y][next.x] = roomId;
            queue.push(next);
          }
        }
        roomId += 1;
      }
    }

    return matrix;
  }

  function getRoomIdAt(roomMatrix, tx, ty) {
    if (!roomMatrix[ty] || typeof roomMatrix[ty][tx] === 'undefined') return 1;
    return roomMatrix[ty][tx];
  }

  function collectRoomTiles(roomMatrix) {
    const roomTiles = new Map();
    for (let y = 0; y < roomMatrix.length; y++) {
      for (let x = 0; x < roomMatrix[y].length; x++) {
        const roomId = roomMatrix[y][x];
        if (roomId === 1) continue;
        if (!roomTiles.has(roomId)) roomTiles.set(roomId, []);
        roomTiles.get(roomId).push({ x, y });
      }
    }
    return roomTiles;
  }

